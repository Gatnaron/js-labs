// Запросить у пользователя ввод двух чисел
var num1 = prompt("Введите первое число:");
var num2 = prompt("Введите второе число:");

// Преобразовать введенные значения в числа
num1 = parseFloat(num1);
num2 = parseFloat(num2);

// Проверить, были ли введены числа
if (isNaN(num1) || isNaN(num2)) {
  alert("Пожалуйста, введите числовые значения.");
} else {
  // Выполнить сравнение и вывести результат
  if (num1 > num2) {
    alert(num1 + " больше, чем " + num2);
  } else if (num1 < num2) {
    alert(num1 + " меньше, чем " + num2);
  } else {
    alert("Числа равны.");
  }
}
